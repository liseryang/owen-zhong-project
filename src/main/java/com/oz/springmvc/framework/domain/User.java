package com.oz.springmvc.framework.domain;

public class User {
	private Long id;
	private String name;
	private String pw;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String ps) {
		this.pw = ps;
	}
	
}
